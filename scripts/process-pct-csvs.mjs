// Combines the 5 .tmp-pct-trail-batch-*.csv files written by the
// Stage 18 % agents into:
//   - data/trail-pct-apply.sql        (HIGH + MEDIUM rows; UPDATE statements)
//   - data/trail-pct-review.csv       (LOW rows; manual review later)
//   - data/trail-pct-skip.csv         (SKIP rows; for visibility)
//
// Apply policy mirrors process-trail-csvs.mjs but writes to the new
// difficulty_pct_* columns. Strict NULL-over-guess: only HIGH/MEDIUM
// auto-applied; LOW (count-derived) saved for manual review.
//
// Run from repo root: node scripts/process-pct-csvs.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.join(__dirname, "..");
const TMP_DIR = REPO_ROOT;
const OUT_DIR = path.join(REPO_ROOT, "data");

function parseCsvRow(line) {
  const cells = [];
  let cur = "";
  let q = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') {
      if (q && line[i + 1] === '"') {
        cur += '"';
        i++;
        continue;
      }
      q = !q;
      continue;
    }
    if (c === "," && !q) {
      cells.push(cur);
      cur = "";
      continue;
    }
    cur += c;
  }
  cells.push(cur);
  return cells;
}

function parseCsv(text) {
  const ls = text.split(/\r?\n/).filter(Boolean);
  const h = parseCsvRow(ls[0]);
  return ls.slice(1).map((l) => {
    const c = parseCsvRow(l);
    const r = {};
    h.forEach((k, i) => (r[k] = c[i] ?? ""));
    return r;
  });
}

function csvCell(v) {
  if (v == null) return "";
  const s = String(v);
  if (s.includes(",") || s.includes('"') || s.includes("\n")) {
    return '"' + s.replace(/"/g, '""') + '"';
  }
  return s;
}
const csvLine = (cells) => cells.map(csvCell).join(",");

function asPct(v) {
  if (v == null) return null;
  const s = String(v).trim();
  if (s === "") return null;
  const n = Number(s);
  if (!Number.isFinite(n) || !Number.isInteger(n) || n < 0 || n > 100) return null;
  return n;
}
function asInt(v) {
  if (v == null) return null;
  const s = String(v).trim();
  if (s === "") return null;
  const n = Number(s);
  if (!Number.isFinite(n) || !Number.isInteger(n) || n < 0) return null;
  return n;
}

const all = [];
for (let i = 1; i <= 5; i++) {
  const p = path.join(TMP_DIR, `.tmp-pct-trail-batch-${i}.csv`);
  if (!fs.existsSync(p)) {
    console.error(`Missing batch file: ${p}`);
    process.exit(1);
  }
  parseCsv(fs.readFileSync(p, "utf8")).forEach((r) => {
    r._batch = i;
    all.push(r);
  });
}
console.log(`Loaded ${all.length} rows across 5 batches.`);

const buckets = { HIGH: [], MEDIUM: [], LOW: [], SKIP: [], OTHER: [] };
for (const r of all) {
  const conf = (r.confidence || "").toUpperCase();
  if (buckets[conf]) buckets[conf].push(r);
  else buckets.OTHER.push(r);
}
console.log(
  `Confidence: HIGH=${buckets.HIGH.length}  MEDIUM=${buckets.MEDIUM.length}  LOW=${buckets.LOW.length}  SKIP=${buckets.SKIP.length}  OTHER=${buckets.OTHER.length}`,
);

if (buckets.OTHER.length > 0) {
  console.warn(`⚠  ${buckets.OTHER.length} rows with unrecognized confidence:`);
  buckets.OTHER.slice(0, 10).forEach((r) =>
    console.warn(`  ${r.slug}: confidence="${r.confidence}"`),
  );
}

const approved = [];
let droppedBlank = 0;
let droppedBadSum = 0;

for (const r of [...buckets.HIGH, ...buckets.MEDIUM]) {
  const b = asPct(r.pct_beginner);
  const i = asPct(r.pct_intermediate);
  const a = asPct(r.pct_advanced);
  const e = asPct(r.pct_expert);
  const p = asInt(r.terrain_park_count);
  const hasAnyPct = b != null || i != null || a != null || e != null;
  const hasAny = hasAnyPct || p != null;
  if (!hasAny) {
    droppedBlank++;
    continue;
  }
  // Sanity: present pcts must sum to 95-105 (rounding + 3-tier
  // resorts where expert is intentionally blank).
  if (hasAnyPct) {
    const sum = (b ?? 0) + (i ?? 0) + (a ?? 0) + (e ?? 0);
    if (sum < 95 || sum > 105) {
      console.warn(`⚠  ${r.slug}: pct sum=${sum} (outside 95-105) — dropping`);
      droppedBadSum++;
      continue;
    }
  }
  approved.push({
    slug: r.slug,
    name: r.name,
    confidence: r.confidence,
    pct_b: b,
    pct_i: i,
    pct_a: a,
    pct_e: e,
    park: p,
    has_park: p != null && p > 0 ? true : null,
    sources: r.sources,
    notes: r.notes,
  });
}

console.log(`Approved for DB apply: ${approved.length} rows.`);
if (droppedBlank > 0) console.log(`  (${droppedBlank} HIGH/MEDIUM rows had all cells blank — dropped.)`);
if (droppedBadSum > 0) console.warn(`  (${droppedBadSum} rows dropped — pct sum outside 95-105.)`);

const sqlLines = [
  "-- Stage 18 difficulty-mix sync — generated by scripts/process-pct-csvs.mjs",
  "-- Apply via Supabase SQL editor.",
  "-- Source: parallel WebFetch agents, resort sites + Wikipedia, cross-verified.",
  "-- Policy: only HIGH + MEDIUM confidence; LOW (count-derived) → review CSV.",
  `-- Total updates: ${approved.length}`,
  "",
  "BEGIN;",
  "",
];

for (const u of approved) {
  const sets = [];
  if (u.pct_b != null) sets.push(`difficulty_pct_beginner = ${u.pct_b}`);
  if (u.pct_i != null) sets.push(`difficulty_pct_intermediate = ${u.pct_i}`);
  if (u.pct_a != null) sets.push(`difficulty_pct_advanced = ${u.pct_a}`);
  if (u.pct_e != null) sets.push(`difficulty_pct_expert = ${u.pct_e}`);
  if (u.park != null) {
    sets.push(`terrain_park_count = ${u.park}`);
    if (u.park > 0) sets.push(`has_terrain_park = true`);
  }
  if (sets.length === 0) continue;
  const slugSql = String(u.slug).replace(/'/g, "''");
  sqlLines.push(`-- [${u.confidence}] ${u.name}`);
  if (u.notes && u.notes.trim()) {
    sqlLines.push(`--   note: ${u.notes.trim().replace(/\n/g, " ")}`);
  }
  sqlLines.push(`UPDATE resorts SET ${sets.join(", ")} WHERE slug = '${slugSql}';`);
  sqlLines.push("");
}

sqlLines.push("COMMIT;");
sqlLines.push("");

const sqlPath = path.join(OUT_DIR, "trail-pct-apply.sql");
fs.mkdirSync(OUT_DIR, { recursive: true });
fs.writeFileSync(sqlPath, sqlLines.join("\n"), "utf8");
console.log(`Wrote SQL: ${path.relative(REPO_ROOT, sqlPath)}`);

// Review CSV — LOW rows for manual top-up.
const reviewHeaders = [
  "slug",
  "name",
  "pct_beginner",
  "pct_intermediate",
  "pct_advanced",
  "pct_expert",
  "terrain_park_count",
  "confidence",
  "sources",
  "notes",
];
const reviewLines = [csvLine(reviewHeaders)];
for (const r of buckets.LOW) {
  reviewLines.push(
    csvLine([
      r.slug,
      r.name,
      r.pct_beginner,
      r.pct_intermediate,
      r.pct_advanced,
      r.pct_expert,
      r.terrain_park_count,
      r.confidence,
      r.sources,
      r.notes,
    ]),
  );
}
const reviewPath = path.join(OUT_DIR, "trail-pct-review.csv");
fs.writeFileSync(reviewPath, reviewLines.join("\n"), "utf8");
console.log(`Wrote review CSV: ${path.relative(REPO_ROOT, reviewPath)} (${buckets.LOW.length} rows)`);

const skipLines = [csvLine(["slug", "name", "notes"])];
for (const r of buckets.SKIP) {
  skipLines.push(csvLine([r.slug, r.name, r.notes]));
}
const skipPath = path.join(OUT_DIR, "trail-pct-skip.csv");
fs.writeFileSync(skipPath, skipLines.join("\n"), "utf8");
console.log(`Wrote skip CSV: ${path.relative(REPO_ROOT, skipPath)} (${buckets.SKIP.length} rows)`);

console.log("");
console.log(`Coverage projection (after BOTH the schema migration AND this SQL):`);
console.log(`  - 115 from Stage 16 backfill (existing trail counts → %)`);
console.log(`  - +${approved.length} from agent verified % data`);
console.log(`  - = ${115 + approved.length} / 451 (${(((115 + approved.length) / 451) * 100).toFixed(1)}%) projected`);
