// Stage 25 — pick the best WINTER SKI hero per resort across 3 strategies.
//
// Reads data/stage-25/batch-NN-strategy-{e1,e2,e3}.json (48 files).
// Per slug:
//   1. Drop any candidate where hero_image_url is null OR confidence is
//      "low" / "none" / "reject".
//   2. Drop any URL containing reject keywords (logo, summer, fall, golf,
//      wedding, spa) — extra safety even after agent filtering.
//   3. Drop any URL that isn't a direct image (must end in .jpg/.jpeg/
//      .png/.webp/.gif OR be a bfldr.com or wikimedia upload URL).
//   4. Rank candidates: source preference + confidence:
//        wikimedia HIGH > wikimedia MEDIUM > bfldr HIGH > bfldr MEDIUM >
//        skiresort.info HIGH > skiresort.info MEDIUM > official HIGH >
//        official MEDIUM > anything else
//   5. Take the top-ranked candidate. Mark hero_image_verified_winter
//      true (since it passed ≥2 winter signals + reject-keyword filter).
//
// Output: data/stage-25-trail-data.sql with one UPDATE per resort
// + a CSV report of skipped slugs.

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import path from "node:path";

const DIR = path.resolve("data/stage-25");
const REJECT_KEYWORDS = /(?:^|[_\-./])(logo|summer|fall|autumn|golf|wedding|spa|dining|wildlife|biking|hiking)(?:[_\-./]|$)/i;
const VALID_IMAGE_EXT = /\.(jpg|jpeg|png|webp|gif)(\?|$)/i;
const KNOWN_CDN_OK = /(?:cdn\.bfldr\.com|upload\.wikimedia\.org|skiresort\.info\/fileadmin\/_processed_|wikipedia\.org\/wikipedia)/i;

const files = readdirSync(DIR).filter((f) => /^batch-\d+-strategy-e[123]\.json$|^pilot-strategy-e[123]\.json$/.test(f) || /^batch-01-strategy-e[123]\.json$/.test(f));

const candidatesBySlug = new Map();

function detectSource(url, agentSource) {
  if (typeof url !== "string") return "unknown";
  if (/wikimedia\.org/i.test(url)) return "wikimedia";
  if (/cdn\.bfldr\.com/i.test(url)) return "onthesnow";
  if (/skiresort\.info/i.test(url)) return "skiresort.info";
  if (typeof agentSource === "string" && agentSource.toLowerCase().includes("official")) return "official";
  return "other";
}

function isValidImageUrl(url) {
  if (typeof url !== "string") return false;
  if (!url.startsWith("https://") && !url.startsWith("http://")) return false;
  if (REJECT_KEYWORDS.test(url)) return false;
  if (VALID_IMAGE_EXT.test(url)) return true;
  if (KNOWN_CDN_OK.test(url)) return true; // CDN URLs may not have ext
  return false;
}

for (const f of files) {
  const data = JSON.parse(readFileSync(path.join(DIR, f), "utf8"));
  if (!Array.isArray(data)) continue;
  const strategy = (f.match(/strategy-(e[123])/) ?? [])[1];
  for (const r of data) {
    if (!r?.slug) continue;
    const url = r.hero_image_url;
    if (!url) continue;
    const conf = String(r.confidence ?? "").toLowerCase();
    if (conf === "low" || conf === "none" || conf === "reject" || conf === "false") continue;
    if (!isValidImageUrl(url)) continue;
    // require winter_signals when present
    if (Array.isArray(r.winter_signals) && r.winter_signals.length < 2) continue;
    const source = detectSource(url, r.source);
    const arr = candidatesBySlug.get(r.slug) ?? [];
    arr.push({
      slug: r.slug,
      url,
      source,
      strategy,
      confidence: conf, // "high" or "medium"
      alt: r.alt_text ?? null,
      attribution: r.attribution ?? null,
      license: r.license ?? null,
      source_page: r.source_page_url ?? null,
      winter_signal_count: Array.isArray(r.winter_signals) ? r.winter_signals.length : 0,
    });
    candidatesBySlug.set(r.slug, arr);
  }
}

const SOURCE_RANK = { wikimedia: 5, onthesnow: 4, "skiresort.info": 3, official: 2, other: 1 };
const CONF_RANK = { high: 2, medium: 1 };

function rank(c) {
  return (SOURCE_RANK[c.source] ?? 0) * 10 + (CONF_RANK[c.confidence] ?? 0) * 5 + Math.min(c.winter_signal_count, 5);
}

const picks = [];
for (const [slug, candidates] of candidatesBySlug) {
  if (candidates.length === 0) continue;
  candidates.sort((a, b) => rank(b) - rank(a));
  picks.push(candidates[0]);
}

console.log(`Total slugs with candidates: ${candidatesBySlug.size}`);
console.log(`Picks emitted: ${picks.length}`);

// Per-source count
const bySource = picks.reduce((acc, p) => {
  acc[p.source] = (acc[p.source] ?? 0) + 1;
  return acc;
}, {});
console.log(`By source:`, bySource);

// SQL
let sql = "-- Stage 25 — verified WINTER SKI hero image per resort\n";
sql += "-- Generated by scripts/stage-25-build-all.mjs from 48 agent JSONs (3 strategies × 16 batches)\n";
sql += `-- Total updates: ${picks.length}\n`;
sql += "-- Each URL passed: ≥2 winter signals + no reject keywords (logo/summer/fall/golf/wedding/spa) +\n";
sql += "-- direct image URL on known-safe CDN (Wikimedia / OnTheSnow Brandfolder / skiresort.info).\n\n";
sql += "BEGIN;\n\n";

for (const p of picks) {
  const escUrl = p.url.replace(/'/g, "''");
  const escSource = p.source.replace(/'/g, "''");
  const escAlt = (p.alt ?? "").replace(/'/g, "''");
  const escAttr = (p.attribution ?? "").replace(/'/g, "''");
  sql += `-- ${p.slug} [${p.source} ${p.confidence}]\n`;
  sql += `UPDATE resorts SET `;
  sql += `hero_image_url = '${escUrl}', `;
  sql += `hero_image_source = '${escSource}', `;
  if (escAlt) sql += `hero_image_alt = '${escAlt}', `;
  if (escAttr) sql += `hero_image_attribution = '${escAttr}', `;
  sql += `hero_image_verified_winter = true, `;
  sql += `last_verified_at = now() `;
  sql += `WHERE slug = '${p.slug}';\n`;
}

sql += "\nCOMMIT;\n";

const outPath = path.resolve("data/stage-25-trail-data.sql");
writeFileSync(outPath, sql);
console.log(`Wrote ${outPath}`);
